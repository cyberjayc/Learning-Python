# Augmented Assignment Operator
# Augmented Assignment Operator is an operator that performs an arithmatic operation and assigns the result to the variable.

# Addition
addition = 1
addition += 1
print(addition)

# Multiplication
multiplication = 5
multiplication *= 5
print(multiplication)

# Subtraction
subtraction = 8
subtraction -= 5
print(subtraction)

# Division
division = 10
division /= 2
print(division)

